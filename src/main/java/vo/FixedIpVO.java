package vo;

public class FixedIpVO {

	private String ip;
	private String netMast;
	private String gateway;
	private String dns;
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getNetMast() {
		return netMast;
	}
	public void setNetMast(String netMast) {
		this.netMast = netMast;
	}
	public String getGateway() {
		return gateway;
	}
	public void setGateway(String gateway) {
		this.gateway = gateway;
	}
	public String getDns() {
		return dns;
	}
	public void setDns(String dns) {
		this.dns = dns;
	}
	
	
}
