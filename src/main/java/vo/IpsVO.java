package vo;

public class IpsVO {

	private String index;
	private String ipsName;
	private String ipsStr;
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public String getIpsName() {
		return ipsName;
	}
	public void setIpsName(String ipsName) {
		this.ipsName = ipsName;
	}
	public String getIpsStr() {
		return ipsStr;
	}
	public void setIpsStr(String ipsStr) {
		this.ipsStr = ipsStr;
	}
	
}
