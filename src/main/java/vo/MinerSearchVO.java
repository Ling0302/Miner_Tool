package vo;

public class MinerSearchVO {

	private String worker;//矿工
	private String minAvgCp;//最低值
	private String maxAvgCp;//最高值
	
	public String getWorker() {
		return worker;
	}
	public void setWorker(String worker) {
		this.worker = worker;
	}
	public String getMinAvgCp() {
		return minAvgCp;
	}
	public void setMinAvgCp(String minAvgCp) {
		this.minAvgCp = minAvgCp;
	}
	public String getMaxAvgCp() {
		return maxAvgCp;
	}
	public void setMaxAvgCp(String maxAvgCp) {
		this.maxAvgCp = maxAvgCp;
	}
	
	
}
